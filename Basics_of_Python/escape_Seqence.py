#In this file we are using escape sequence.
print("Hii,this is \"Akshat Saxena\" ") #We can not use single quotes inside single quotes and double quotes inside double quotes.
#If we want to string in double quotes then we can use '\' before and after the double quotes.
print('Hii,this is "Akshat Saxena" ')

print("Hii,this is \Akshat Saxena\ ") #It gives error because \ is used for escape sequence.


print("Hii,this is \nAkshat Saxena\n ") #\n is used for new line.
print("I am \\Akshat Saxena\\") #\\ is used for backslash.
print("I am \tAkshat Saxena") #\t is used for tab space.


